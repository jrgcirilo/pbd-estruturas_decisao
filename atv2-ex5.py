dia_semana=int(input('Digite um numero:'))
if (dia_semana==1):
    print('1-Domingo')
elif (dia_semana==2):
    print('2-Segunda-feira')
elif (dia_semana==3):
    print('3-Terça-feira')
elif (dia_semana==4):
    print('4-Quarta-feira')
elif (dia_semana==5):
    print('5-Quinta-feira')
elif (dia_semana==6):
    print('6-Sexta-feira')
elif (dia_semana==7):
    print('7-Sábado')
else:
    print('Digite um número entre 1 e 7')